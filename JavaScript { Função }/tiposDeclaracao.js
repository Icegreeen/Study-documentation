
// function declaration

function soma(x, y) {
    return x + y
}

// function expression: declarar uma função em uma variável ou constante.

const sub = function (x, y) {
    return x - y
}

// named function expression 

const mult = function mult(x, y) { // A vantagem de estar nomeada talvez possa ajudar quando estiver debugando.
    return x * y 
}

